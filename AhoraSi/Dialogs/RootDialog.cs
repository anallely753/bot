               nameof(TextPrompt),
               new PromptOptions { 
                   Prompt = MessageFactory.Text("Para validar tu identidad por favor ingresa tu fecha de nacimiento (dd/mm/aaaa) "),
                   RetryPrompt = MessageFactory.Text("La fecha no coincide, por favor intenta de nuevo")
               },
               cancellationToken
           );
        }

