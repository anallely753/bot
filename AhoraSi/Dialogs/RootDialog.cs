        private async Task<DialogTurnResult> ConfirmOptions(WaterfallStepContext stepContext, CancellationToken cancellationToken)
        {
            var option = stepContext.Context.Activity.Text;
            switch (option)
            {
                case "Nómina":
                    await stepContext.Context.SendActivityAsync($"Para consultar información de tu nómina ingresa al siguiente link: https://bepensa.csod.com/client/bepensa/default.aspx", cancellationToken: cancellationToken);
                    break;
                case "Infonavit":
                    await stepContext.Context.SendActivityAsync($"Para ver información de tu Infonavit ingresa al siguiente link: https://bepensa.csod.com/client/bepensa/default.aspx", cancellationToken: cancellationToken);
                    break;
                case "Vacaciones":
                    await stepContext.Context.SendActivityAsync("Caja de ahorro", cancellationToken: cancellationToken);
                    break;
                case "Caja de ahorro":
                    await stepContext.Context.SendActivityAsync($"Para ver tu caja de ahorro ingresa al siguiente link: https://bepensa.csod.com/client/bepensa/default.aspx", cancellationToken: cancellationToken);
                    break;
                case "Beneplus":
                    await stepContext.Context.SendActivityAsync($"Para ingresar a Beneplus al siguiente link: https://bepensa.csod.com/client/bepensa/default.aspx", cancellationToken: cancellationToken);
                    break;
                case "Formatos":
                    return await stepContext.BeginDialogAsync($"{nameof(RootDialog)}.nominaDialog", null, cancellationToken);
                default:
                    await stepContext.Context.SendActivityAsync("Opción no valida", cancellationToken: cancellationToken);
                    break;
            }
            return await stepContext.NextAsync(null, cancellationToken);
        }
        private async Task<DialogTurnResult> RegresoAMenu(WaterfallStepContext stepContext, CancellationToken cancellationToken)
        {
            return await ConfirmButtonDialog.ShowOption(stepContext, cancellationToken);
        }

        private async Task<bool> ConfirmValidate(PromptValidatorContext<bool> promptContext, CancellationToken cancellationToken)
        {
            var option = promptContext.Recognized.Value;
            if (option)
                await promptContext.Context.SendActivityAsync("Ok", cancellationToken: cancellationToken);
            else
                await promptContext.Context.SendActivityAsync("¡Hasta pronto!", cancellationToken: cancellationToken);
            return true;
        }

